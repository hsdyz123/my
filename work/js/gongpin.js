function gp1() {
  $('#gp11').text(
    ($('#ipt_gongpin1').val() - 0.005 + Math.random() * 0.01).toFixed(3)
  )
  $('#gp12').text(
    ($('#ipt_gongpin1').val() - 0.005 + Math.random() * 0.01).toFixed(3)
  )
  $('#gp13').text(
    ($('#ipt_gongpin1').val() - 0.005 + Math.random() * 0.01).toFixed(3)
  )
  $('#gp14').text(
    (
      (parseFloat($('#gp11').text()) +
        parseFloat($('#gp12').text()) +
        parseFloat($('#gp13').text())) /
      3
    ).toFixed(3)
  )
}
